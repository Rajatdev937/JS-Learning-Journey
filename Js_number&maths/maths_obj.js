//In JavaScript, Math is a built-in object used for mathematical operations.
// Provides math-related properties & functions
// Used for calculations like power, square root, rounding, random numbers, etc.

//1.Constant values :

console.log(Math.PI);//value of pie
console.log(Math.PI.toFixed(3));//round-off to 3 decimal places

console.log(Math.E);//value of Euler's number
console.log(Math.E.toFixed(2));//round-off to 2 decimal places

console.log("\n");

//2.Root and Exponetial powers :
console.log(Math.sqrt(25));//returns sq root of 25
console.log(Math.pow(5,2));//5 raise to power 2

console.log("\n");

//3.Absoulte values: 

console.log(Math.abs(-3));//returns a positive int


console.log("\n");


//4.Rounding off:

console.log(Math.ceil(4.1));//next int
console.log(Math.floor(4.1));//previous int
console.log(Math.round(5.5));
console.log(Math.floor(7.75));//Returns Greatest int of the number.

console.log("\n");

//5.Min&Max number : 

console.log(Math.min(2,3,4));//returns minimum value
console.log(Math.max(2,3,4));//returns maximum value

console.log("\n");


//6. Random Int :

console.log(Math.random())//return any decimal number bw  0 <= n <=1
console.log("\n");


//7.Logrithm and trignometric values : 
console.log(Math.log(2));
console.log(Math.log10(10));

console.log(Math.sin(Math.PI/2));//value of sin 90  in radian.
console.log(Math.cos(0));//value of cos 0 .
console.log(Math.tan(Math.PI/4));//value of tan 45.





