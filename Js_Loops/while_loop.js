//WHILE LOOP :  Runs a given set of statement or code until the condition becomes true .
// used mostly when you dont know when will the loop will end.
//ex- 
let i=0;
while(i<=10){
console.log(i);
i++;
}
console.log("\n");

//To print elements of an array:

let arr=[10,20,30,40,50];
let j=0;
while(j<arr.length){
console.log(arr[j]);
j++;
}