//Increment & Decrement Operators 

// ++ → increment by 1

// -- → decrement by 1

//1.INCREMENT OPERTOR :

//1.1 PRE-increment - First inc the number and then prints .
//ex-
let a=10;
let b=++a;//inc 10 to 11 and prints
console.log(b);

//1.2 POST-increment - First prints the number and then inc it .
let c=10;
let d=c++;//prints 10 first and then inc in memory
console.log(d);
console.log(c);//gives 11

//2.DECREMENT OPERATOR :

//2.1 PRE-decrement -  First dec then prints.
let y=15;
let x=--y;
console.log(x);

//2.2 POST - decrement - First prints then decrease.
let v=15;
let w=v--;
console.log(w);
console.log(v);

